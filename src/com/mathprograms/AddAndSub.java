package com.mathprograms;

public class AddAndSub {

	public void add(int n, int m) {

		int sum = n + m;
		System.out.println("addition of two integers : " + sum);
	}

	public void sub(int n, int m) {
		int a = n;
		int b = m;

		int sub = a - b;
		System.out.println("subtraction of the two integers : " + sub);
	}

}
