package com.mathprograms;

public class MulAndDiv {

	public void mul(int a, int p) {

		int mul = a * p;
		System.out.println("multiplication of two integers : " + mul);
	}

	public void div(float p, float q) {
		float c = p;
		float l = q;

		float div = c / l;
		System.out.println("division of two integers : " + div);
	}
}
